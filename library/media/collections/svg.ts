const HEROICONS_BASE_URL =
  'https://raw.githubusercontent.com/tailwindlabs/heroicons/master/src/24/outline';
const SIMPLE_ICONS_BASE_URL = 'https://cdn.simpleicons.org';

function heroiconsRef(iconName: string) {
  return createMediaRef('url', `${HEROICONS_BASE_URL}/${iconName}.svg`);
}

function simpleIconsRef(brandName: string) {
  return createMediaRef('url', `${SIMPLE_ICONS_BASE_URL}/${brandName}`);
}

function createHeroiconSvgItem(
  idSuffix: string,
  label: string,
  iconName: string,
  tags: string[],
) {
  return {
    id: `svg-heroicons-${idSuffix}`,
    label,
    category: 'icons',
    kind: 'svg' as const,
    ref: heroiconsRef(iconName),
    mimeType: 'image/svg+xml',
    tags: ['svg', 'icon', 'heroicons', ...tags, 'outline'],
  };
}

function createSimpleLogoSvgItem(
  idSuffix: string,
  label: string,
  brandName: string,
  tags: string[],
) {
  return {
    id: `svg-logo-${idSuffix}`,
    label,
    category: 'logos',
    kind: 'svg' as const,
    ref: simpleIconsRef(brandName),
    mimeType: 'image/svg+xml',
    tags: ['svg', 'logo', ...tags],
  };
}

