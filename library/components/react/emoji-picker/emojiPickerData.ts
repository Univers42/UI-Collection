export const EMOJI_PICKER_GROUPS = [
  'Smileys & Emotion',
  'People & Body',
  'Animals & Nature',
  'Food & Drink',
  'Travel & Places',
  'Activities',
  'Objects',
  'Symbols',
  'Flags',
] as const;

export type EmojiPickerGroup = (typeof EMOJI_PICKER_GROUPS)[number];
export type EmojiPickerGroupValue = EmojiPickerGroup | (string & {});

export interface EmojiPickerItem {
  id: string;
  label: string;
  value: string;
  aliases?: string[];
  keywords?: string[];
  group?: EmojiPickerGroupValue;
  localizedLabels?: Record<string, string>;
  src?: string;
}

type EmojiPickerSeed = readonly [
  id: string,
  label: string,
  value: string,
  keywords?: string[],
  aliases?: string[],
];

function titleToSpanishLabel(label: string): string {
  return label
    .replace(/^Flag:\s+/u, 'Bandera: ')
    .replace(/\bFace\b/gu, 'cara')
    .replace(/\bHand\b/gu, 'mano')
    .replace(/\bHands\b/gu, 'manos')
    .replace(/\bHeart\b/gu, 'corazon')
    .replace(/\bFlag\b/gu, 'bandera')
    .toLowerCase();
}

function createEmojiPickerItems(
  group: EmojiPickerGroup,
  seeds: readonly EmojiPickerSeed[],
): EmojiPickerItem[] {
  return seeds.map(([id, label, value, keywords = [], aliases = []]) => ({
    id,
    label,
    value,
    group,
    aliases: aliases.length > 0 ? aliases : [id.replace(/-/gu, ' ')],
    keywords,
    localizedLabels: {
      es: titleToSpanishLabel(label),
    },
  }));
}

